class Channel {
  constructor() {
    this.subscribers = {}
  }

  emit(eventName, data) {
    if (!Array.isArray(this.subscribers[eventName])) {
      return
    }
    this.subscribers[eventName].forEach((callback) => {
      callback(data)
    })
  }

  on(eventName, callback) {
    if (!Array.isArray(this.subscribers[eventName])) {
      this.subscribers[eventName] = []
    }
    this.subscribers[eventName].push(callback)
  }
}

export default new Channel()
